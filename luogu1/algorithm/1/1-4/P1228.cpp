#include <bits/stdc++.h>
using namespace std;
int l[1025][1025];
int k, x, y;
void f(int x1, int y1, int x2, int y2, int n)
{
    if (n == 1)
        return;
    if (x1 - x2 < (n >> 1))
    {
        if (y1 - y2 < (n >> 1))
        {
            cout << (x2 + (n >> 1)) << ' ' << (y2 + (n >> 1)) << ' ' << 1 << endl;
            f(x1, y1, x2, y2, (n >> 1));
            f(x2 + (n >> 1) - 1, y2 + (n >> 1), x2, y2 + (n >> 1), (n >> 1));
            f(x2 + (n >> 1), y2 + (n >> 1) - 1, x2 + (n >> 1), y2, (n >> 1));
            f(x2 + (n >> 1), y2 + (n >> 1), x2 + (n >> 1), y2 + (n >> 1), (n >> 1));
        }
        else
        {
            cout << (x2 + (n >> 1)) << ' ' << (y2 + (n >> 1) - 1) << ' ' << 2 << endl;
            f(x2 + (n >> 1) - 1, y2 + (n >> 1) - 1, x2, y2, (n >> 1));
            f(x1, y1, x2, y2 + (n >> 1), (n >> 1));
            f(x2 + (n >> 1), y2 + (n >> 1) - 1, x2 + (n >> 1), y2, (n >> 1));
            f(x2 + (n >> 1), y2 + (n >> 1), x2 + (n >> 1), y2 + (n >> 1), (n >> 1));
        }
    }
    else
    {
        if (y1 - y2 < (n >> 1))
        {
            cout << (x2 + (n >> 1) - 1) << ' ' << (y2 + (n >> 1)) << ' ' << 3 << endl;
            f(x2 + (n >> 1) - 1, y2 + (n >> 1) - 1, x2, y2, (n >> 1));
            f(x2 + (n >> 1) - 1, y2 + (n >> 1), x2, y2 + (n >> 1), (n >> 1));
            f(x1, y1, x2 + (n >> 1), y2, (n >> 1));
            f(x2 + (n >> 1), y2 + (n >> 1), x2 + (n >> 1), y2 + (n >> 1), (n >> 1));
        }
        else
        {
            cout << (x2 + (n >> 1) - 1) << ' ' << (y2 + (n >> 1)-1) << ' ' << 4 << endl;
            f(x2 + (n >> 1) - 1, y2 + (n >> 1) - 1, x2, y2, (n >> 1));
            f(x2 + (n >> 1) - 1, y2 + (n >> 1), x2, y2 + (n >> 1), (n >> 1));
            f(x2 + (n >> 1), y2 + (n >> 1)-1, x2 + (n >> 1), y2, (n >> 1));
            f(x1, y1, x2 + (n >> 1), y2 + (n >> 1), (n >> 1));
        }
    }
}
int main()
{
    cin >> k >> x >> y;
    l[x][y] = -1;
    f(x, y, 1, 1, 1 << k);
    return 0;
}